import { useState } from "react";
import { motion } from "framer-motion";
import "./styles.css";

const itemVariants = {
  open: {
    opacity: 1,
    y: 0,
    transition: { type: "spring", stiffness: 300, damping: 24 },
  },
  closed: { opacity: 0, y: 20, transition: { duration: 0.2 } },
};

const SelectComponent = ({ TagList, onTagSelect }) => {
  const [isOpen, setIsOpen] = useState(false);

  const handleTagClick = (tag) => {
    onTagSelect(tag); // Call the parent function when a tag is selected
    setIsOpen(false); // Close the menu after selection
  };

  return (
    <motion.nav
      initial={false}
      animate={isOpen ? "open" : "closed"}
      className="menu"
    >
      <motion.button
        whileTap={{ scale: 0.97 }}
        onClick={() => setIsOpen(!isOpen)}
      >
        Filter Tag
        <motion.div
          variants={{
            open: { rotate: 180 },
            closed: { rotate: 0 },
          }}
          transition={{ duration: 0.2 }}
          style={{ originY: 0.55 }}
        >
          <svg width="15" height="15" viewBox="0 0 20 20">
            <path d="M0 7 L 20 7 L 10 16" />
          </svg>
        </motion.div>
      </motion.button>
      <motion.ul
        variants={{
          open: {
            clipPath: "inset(0% 0% 0% 0% round 10px)",
            transition: {
              type: "spring",
              bounce: 0,
              duration: 0.7,
              delayChildren: 0.3,
              staggerChildren: 0.05,
            },
          },
          closed: {
            clipPath: "inset(10% 50% 90% 50% round 10px)",
            transition: {
              type: "spring",
              bounce: 0,
              duration: 0.3,
            },
          },
        }}
        style={{ pointerEvents: isOpen ? "auto" : "none" }}
      >
        {TagList.map((tag) => (
          <motion.li key={tag} value={tag} variants={itemVariants} onClick={()=> handleTagClick(tag)}>
            {tag}
          </motion.li>
        ))}
      </motion.ul>
    </motion.nav>
  );
};

export default SelectComponent;
